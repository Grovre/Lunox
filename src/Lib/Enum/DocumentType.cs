namespace Lunox.Library.Enum
{
    #region DocumentTypeEnum

    /// <summary>
    /// 
    /// </summary>
    public enum DocumentType
    {
        /// <summary>
        /// A PDF file is a multi-platform document created by Adobe Acrobat or another PDF application.
        /// The PDF format is commonly used for saving documents and publications in a standard format
        /// that can be viewed on multiple platforms. In many cases, PDF files are created from existing
        /// documents instead of from scratch.
        /// </summary>
        PDF,
        /// <summary>
        /// A DOC file is a word processing document created by Microsoft Word or exported by another
        /// word processing program, such as OpenOffice Writer or Apple Pages. It may contain formatted
        /// text, images, tables, graphs, charts, page formatting, and print settings.
        /// </summary>
        DOC,
        /// <summary>
        /// A DOCX file is a document created by Microsoft Word or exported by another word processing
        /// program, such as OpenOffice Writer or Apple Pages. It contains formatted text and may also
        /// include images, tables, charts, and other document elements. DOCX files are widely used in
        /// home, academic, and business environments for letters, resumes, invitations, newsletters,
        /// and other documents.
        /// </summary>
        DOCX
    }

    #endregion
